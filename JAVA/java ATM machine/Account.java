
public class Account {
	//variables
	private int id;
	private double balance;
	
	//constructors
	Account()
	{
		this(0, 0.0);
	}
	Account(int numberOfId, double numberOfBalance)
	{
		id = numberOfId;
		balance = numberOfBalance;
	}
	
	//methods
	//getter
	public int getId()
	{
		return id;
	}
	public double getBalance()
	{
		return balance;
	}
	//setter
	public void setId(int sId)
	{
		id = sId;
	}
	public void setBalance(double sBalance)
	{
		balance = sBalance;
	}
}
